/*
** LIBRARY: <string.h>
** SYNOPSIS: locate character in string (last occurence)
** SINOPSE : localizar um caractere em string (última ocorrência)
**
** DESCRIPTION:
** 		The strrchr() function locates the last occurrence of c (converted to a
**	char) in the string s.  If c is `\0', strrchr() locates the terminating `\0'.
**
** DESCRIÇÃO:
** 		A função strrchr() localiza a última ocorrência de 'c' (convertida em um
**  char) na string 's'. Se 'c' for `\0', strrchr() localiza o final '\0'.
*/

#include <stdio.h>
#include "ft_strlen.h"

char	*ft_strrchr(const char *s, int c)
{
	unsigned int	i;
	char			*last_occurence;

	last_occurence = NULL;
	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			last_occurence = (char *)&s[i];
		i++;
	}
	if ((char)c == s[i])
		return ((char *)&s[i]);
	return (last_occurence);
}
