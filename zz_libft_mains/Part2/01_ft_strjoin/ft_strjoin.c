/*
** LIBRARY: N/A
** SYNOPSIS: concatenate two strings into a new string (with malloc)
** SINOPSE : concatenar duas strings em uma nova string (usando malloc)
**
** DESCRIPTION:
** 		Allocates (with malloc(3)) and returns a new string, which is the
**	result of the concatenation of ’s1’ and ’s2’.
**
** DESCRIÇÃO:
** 		Aloca (com malloc(3)) e retorna uma nova string, que é resultado
**  da concatenação de ’s1’ e ’s2’.
*/

#include <stdio.h>
#include <stdlib.h>
#include "ft_strjoin.h"
#include "ft_strlen.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*new_str;
	int		i;
	int		j;

	new_str = (char *)malloc(ft_strlen(s1) + ft_strlen(s2) + 1);
	if (!s1 || !s2 || !(new_str))
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		new_str[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j] != '\0')
		new_str[i++] = s2[j++];
	new_str[i] = '\0';
	return (new_str);
}
